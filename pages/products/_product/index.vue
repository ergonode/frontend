<template>
    <ProductPage
        title="New Product"
        @dismiss="onDismiss"
        @create="onCreate" />
</template>

<script>
import { mapState, mapActions } from 'vuex';
import { getMappedTemplateID } from '~/model/mappers/productMapper';
import { getMappedCategoryID } from '~/model/mappers/categoryMapper';

export default {
    name: 'Settings',
    middleware: ['tab/redirectToProductGeneral'],
    components: {
        ProductPage: () => import('~/components/Pages/ProductPage'),
    },
    computed: {
        ...mapState('productsDraft', {
            sku: (state) => state.sku,
            template: (state) => state.template,
            templates: (state) => state.templates,
            selectedCategories: (state) => state.selectedCategories,
            categories: (state) => state.categories,
        }),
    },
    methods: {
        ...mapActions('productsDraft', [
            'createProduct',
        ]),
        ...mapActions('validations', [
            'onError',
            'removeValidationErrors',
        ]),
        onDismiss() {
            this.$router.back();
        },
        onCreateProductSuccess(id) {
            this.removeValidationErrors();
            this.$addAlert({ type: 'success', message: 'Product created' });
            this.$router.push({
                name: 'product-edit-id',
                params: {
                    id,
                },
            });
        },
        onCreate() {
            this.removeValidationErrors();
            const data = {
                sku: this.sku,
                templateId: getMappedTemplateID(
                    this.templates,
                    this.template,
                ),
            };
            if (this.selectedCategories.length > 0) {
                data.categoryIds = getMappedCategoryID(
                    this.categories,
                    this.selectedCategories,
                );
            }
            this.createProduct({
                data,
                onSuccess: this.onCreateProductSuccess,
                onError: this.onError,
            });
        },
    },
    async fetch({ store }) {
        await Promise.all([
            store.dispatch('productsDraft/clearStorage'),
            store.dispatch('productsDraft/getTemplates'),
            store.dispatch('productsDraft/getCategories'),
        ]);
    },
};
</script>
