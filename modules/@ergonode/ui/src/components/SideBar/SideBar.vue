<template>
    <ExpandingList
        class="side-bar"
        :items="items"
        :expanded="expanded"
        :render-ahead="8">
        <template #header>
            <SideBarStickyHeader>
                <slot name="header">
                    <ListSearchHeader
                        :title="title"
                        :search-value="searchValue"
                        :searchable="searchable"
                        @search="onSearch" />
                </slot>
            </SideBarStickyHeader>
        </template>
        <template #body>
            <slot name="body">
                <slot
                    v-if="isSearchPlaceholderVisible"
                    name="noResultsPlaceholder"
                    :on-clear-search="onClearSearch">
                    <SideBarNoResultsPlaceholder @clear="onClearSearch" />
                </slot>
                <slot
                    v-else-if="isPlaceholderVisible"
                    name="noDataPlaceholder">
                    <SideBarNoDataPlaceholder />
                </slot>
            </slot>
        </template>
        <template #footer>
            <slot name="footer">
                <SideBarFooter />
            </slot>
        </template>
        <template #item="{ item, onExpand }">
            <slot
                name="item"
                :item="item"
                :on-expand="onExpand" />
        </template>
    </ExpandingList>
</template>

<script>

import ListSearchHeader from '@UI/components/List/ListSearchHeader';
import SideBarFooter from '@UI/components/SideBar/SideBarFooter';
import SideBarNoDataPlaceholder from '@UI/components/SideBar/SideBarNoDataPlaceholder';
import SideBarNoResultsPlaceholder from '@UI/components/SideBar/SideBarNoResultsPlaceholder';
import SideBarStickyHeader from '@UI/components/SideBar/SideBarStickyHeader';
import {
    ExpandingList,
} from 'vue-windowing';

export default {
    name: 'SideBar',
    components: {
        SideBarNoDataPlaceholder,
        SideBarNoResultsPlaceholder,
        SideBarStickyHeader,
        SideBarFooter,
        ListSearchHeader,
        ExpandingList,
    },
    props: {
        /**
         * The title of the component
         */
        title: {
            type: String,
            default: '',
        },
        /**
         * Search value
         */
        searchValue: {
            type: String,
            default: '',
        },
        /**
         * List of items
         */
        items: {
            type: Array,
            default: () => [],
        },
        /**
         * Determines if the component has possibility of search for value
         */
        searchable: {
            type: Boolean,
            default: false,
        },
        /**
         * Determines if the list elements are expanded
         */
        expanded: {
            type: [
                Object,
                Boolean,
            ],
            default: false,
        },
    },
    data() {
        return {
            selectedItems: {},
        };
    },
    computed: {
        isAnyItem() {
            return this.items.length > 0;
        },
        isAnySearchPhrase() {
            return this.searchValue !== '';
        },
        isPlaceholderVisible() {
            return !this.isAnyItem && !this.isAnySearchPhrase;
        },
        isSearchPlaceholderVisible() {
            return !this.isAnyItem && this.isAnySearchPhrase;
        },
        isSelectContentVisible() {
            return this.isAnyItem || this.isAnySearchPhrase;
        },
    },
    methods: {
        onSearch(value) {
            this.$emit('search', value);
        },
        onClearSearch() {
            this.onSearch('');
        },
    },
};
</script>

<style lang="scss" scoped>
    .side-bar {
        max-height: 100%;
    }
</style>
