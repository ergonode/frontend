<template>
    <ChannelPage
        :title="name"
        @remove="onRemove"
        @save="onSave" />
</template>

<script>
import {
    ALERT_TYPE,
} from '@Core/defaults/alerts';
import {
    MODAL_TYPE,
} from '@Core/defaults/modals';
import {
    mapActions,
    mapState,
} from 'vuex';

export default {
    name: 'EditChannel',
    components: {
        ChannelPage: () => import('@Channels/components/Pages/ChannelPage'),
    },
    validate({
        params,
    }) {
        return /\w{8}-\w{4}-\w{4}-\w{4}-\w{12}/.test(params.id);
    },
    async fetch({
        store, params,
    }) {
        await store.dispatch('channels/getChannelById', {
            id: params.id,
        });
    },
    computed: {
        ...mapState('channels', {
            name: state => state.name,
            type: state => state.type,
            configuration: state => state.configuration,
        }),
    },
    methods: {
        ...mapActions('channels', [
            'updateChannel',
            'removeChannel',
        ]),
        ...mapActions('validations', [
            'onError',
            'removeValidationErrors',
        ]),
        onRemove() {
            this.$openModal({
                key: MODAL_TYPE.GLOBAL_CONFIRM_MODAL,
                message: 'Are you sure you want to delete this channel?',
                confirmCallback: () => this.removeChannel({
                    onSuccess: this.onRemoveSuccess,
                }),
            });
        },
        onSave() {
            this.removeValidationErrors();
            this.updateChannel({
                id: this.$route.params.id,
                data: {
                    type: this.type,
                    name: this.name,
                    ...JSON.parse(this.configuration),
                },
                onSuccess: this.onUpdateChannelSuccess,
                onError: this.onError,
            });
        },
        onUpdateChannelSuccess() {
            this.removeValidationErrors();
            this.$addAlert({
                type: ALERT_TYPE.SUCCESS,
                message: 'Channel updated',
            });
        },
        onRemoveSuccess() {
            this.$addAlert({
                type: ALERT_TYPE.SUCCESS,
                message: 'Channel removed',
            });
            this.$router.push({
                name: 'channel-grid',
            });
        },
    },
    head() {
        return {
            title: `${this.name} - Channel - Ergonode`,
        };
    },
};
</script>
