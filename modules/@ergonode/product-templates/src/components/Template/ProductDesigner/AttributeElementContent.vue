<template>
    <ElementContentBase
        :disabled="disabled"
        @mouseover.native="onMouseOver"
        @mouseout.native="onMouseOut">
        <div class="element-content__icon">
            <template v-for="(formComponent, i) in typeIconComponent">
                <Component
                    :is="formComponent.component"
                    :key="i"
                    v-bind="formComponent.props" />
            </template>
        </div>
        <div class="vertical-wrapper">
            <span
                :class="[typeLabelClasses, typeLabelRequireClass]"
                v-text="element.type" />
            <span
                class="element-content__subheader"
                v-text="element.label" />
        </div>
        <div
            v-if="!disabled"
            :class="['element-content__contextual-menu', contextualMenuHoveStateClasses]">
            <ActionIconButton
                :theme="secondaryPlainTheme"
                :size="tinySize"
                :options="contextualMenuItems"
                @input="onSelectValue"
                @focus="onSelectFocus">
                <template #icon="{ color }">
                    <IconDots :fill-color="color" />
                </template>
                <template #option="{ option }">
                    <ListElementDescription>
                        <ListElementTitle
                            :title="option"
                            :size="smallSize" />
                    </ListElementDescription>
                    <ListElementAction :size="smallSize">
                        <CheckBox
                            v-if="option === 'Required'"
                            ref="checkbox"
                            :value="element.required" />
                    </ListElementAction>
                </template>
            </ActionIconButton>
        </div>
    </ElementContentBase>
</template>

<script>
import {
    SIZE,
    THEME,
} from '@Core/defaults/theme';
import ElementContentBase from '@Templates/components/Template/ProductDesigner/ElementContentBase';
import ActionIconButton from '@UI/components/ActionIconButton/ActionIconButton';
import CheckBox from '@UI/components/CheckBox/CheckBox';
import IconDots from '@UI/components/Icons/Others/IconDots';
import ListElement from '@UI/components/List/ListElement';
import ListElementAction from '@UI/components/List/ListElementAction';
import ListElementDescription from '@UI/components/List/ListElementDescription';
import ListElementTitle from '@UI/components/List/ListElementTitle';
import {
    mapActions,
} from 'vuex';

export default {
    name: 'AttributeElementContent',
    components: {
        IconDots,
        ActionIconButton,
        ElementContentBase,
        ListElement,
        ListElementAction,
        ListElementTitle,
        ListElementDescription,
        CheckBox,
    },
    props: {
        scope: {
            type: String,
            default: '',
        },
        changeValues: {
            type: Object,
            default: () => ({}),
        },
        errors: {
            type: Object,
            default: () => ({}),
        },
        index: {
            type: Number,
            required: true,
        },
        element: {
            type: Object,
            required: true,
        },
        disabled: {
            type: Boolean,
            required: true,
        },
    },
    data() {
        return {
            contextualMenuItems: [
                'Required',
                'Remove',
            ],
            isContextualMenuActive: false,
            isHovered: false,
        };
    },
    computed: {
        smallSize() {
            return SIZE.SMALL;
        },
        tinySize() {
            return SIZE.TINY;
        },
        secondaryPlainTheme() {
            return THEME.SECONDARY_PLAIN;
        },
        typeLabelRequireClass() {
            return {
                'element-content--required': this.element.required,
            };
        },
        typeLabelClasses() {
            return 'element-content__header';
        },
        typeIconComponent() {
            const icon = this.$extendedForm({
                key: '@Attributes/components/Lists/AttributeListElement/Icon',
                type: this.element.type,
            });

            if (!icon.length) {
                return [
                    {
                        component: () => import('@Attributes/components/Icons/IconAttributes'),
                    },
                ];
            }

            return icon;
        },
        contextualMenuHoveStateClasses() {
            return {
                'element-content__contextual-menu--hovered': this.isHovered,
            };
        },
    },
    methods: {
        ...mapActions('productTemplate', [
            'updateLayoutElementAtIndex',
            'removeLayoutElementAtIndex',
        ]),
        ...mapActions('feedback', [
            'onScopeValueChange',
        ]),
        onSelectFocus(isFocused) {
            if (!isFocused) this.isHovered = false;

            this.isContextualMenuActive = isFocused;
        },
        onSelectValue(option) {
            switch (option) {
            case 'Required':
                this.updateLayoutElementAtIndex({
                    index: this.index,
                    element: {
                        ...this.element,
                        required: !this.element.required,
                    },
                });

                this.onScopeValueChange({
                    scope: this.scope,
                    fieldKey: 'templateDesigner',
                    value: true,
                });
                break;
            case 'Remove':
                this.removeLayoutElementAtIndex(this.index);

                this.onScopeValueChange({
                    scope: this.scope,
                    fieldKey: 'templateDesigner',
                    value: true,
                });
                break;
            default: break;
            }
        },
        onMouseOver() {
            if (!this.isHovered) this.isHovered = true;
        },
        onMouseOut() {
            if (!this.isContextualMenuActive) this.isHovered = false;
        },
    },
};
</script>

