<template>
    <div :class="pinnedColumnClass">
        <GridCell
            :column="columnIndex"
            :row="rowsOffset"
            :locked="true" />
        <GridCell
            v-if="isBasicFilters"
            :column="columnIndex"
            :row="rowsOffset + basicFiltersOffset"
            :locked="true" />
        <GridEditRowCell
            v-for="(links, index) in rowLinks"
            :key="index"
            :links="links"
            :column="columnIndex"
            :row="rowsOffset + index + basicFiltersOffset + 1"
            :is-selected="isSelectedAllRows
                || selectedRows[rowsOffset + index + basicFiltersOffset + 1]"
            @edit="onEdit" />
    </div>
</template>

<script>
import GridEditRowCell from '@Core/components/Grid/EditCells/GridEditRowCell';

export default {
    name: 'GridColumnEdit',
    components: {
        GridCell: () => import('@Core/components/Grid/GridCell'),
        GridEditRowCell,
    },
    props: {
        columnIndex: {
            type: Number,
            required: true,
        },
        rowsOffset: {
            type: Number,
            default: 0,
        },
        rowLinks: {
            type: Array,
            required: true,
        },
        isPinned: {
            type: Boolean,
            required: true,
        },
        isBasicFilters: {
            type: Boolean,
            default: true,
        },
        isSelectedAllRows: {
            type: Boolean,
            required: true,
        },
        selectedRows: {
            type: Object,
            required: true,
        },
    },
    computed: {
        pinnedColumnClass() {
            return [
                'pinned-column',
                {
                    'pinned-column--right': this.isPinned,
                },
            ];
        },
        basicFiltersOffset() {
            return this.isBasicFilters ? 1 : 0;
        },
    },
    methods: {
        onEdit(route) {
            this.$emit('editRow', route);
        },
    },
};
</script>

<style lang="scss" scoped>
    .pinned-column {
        position: sticky;
        right: 0;
        z-index: $Z_INDEX_LVL_3;
        display: grid;
        box-sizing: border-box;

        &--right {
            box-shadow: 0 3px 5px 0 rgba(0, 0, 0, 0.2);
        }
    }
</style>
